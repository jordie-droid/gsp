    public void arrayCategorie() {
        try {
            String[] entete = {"N°", "Categorie"};
            Object[] row = new Object[2];
            dtm = new DefaultTableModel(null, entete);
            st = new ConnectDataBase().getConnexion().createStatement();
            rs = st.executeQuery("select * from categories");
            while (rs.next()) {
                row[0] = rs.getObject(1);
                row[1] = rs.getObject(2);
                dtm.addRow(row);
            }
            tabCat.setModel(dtm);
            tabCat.getColumnModel().getColumn(0).setPreferredWidth(50);
            tabCat.getColumnModel().getColumn(1).setPreferredWidth(300);
            rs.close();
            st.close();
        } catch (SQLException e) {
            cg.boiteDeDialogue(e.toString(), "erreur");
        }
    }

