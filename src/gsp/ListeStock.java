    public void arrayStock() {
        try {
            String[] entete = {"Produit", "Quantit√©"};
            Object[] row = new Object[2];
            dtm = new DefaultTableModel(null, entete);
            st = new ConnectDataBase().getConnexion().createStatement();
            rs = st.executeQuery("select produit.libprod, stockCourant.qte from produit inner join stockCourant on stockCourant.codprod=produit.codprod");
            while (rs.next()) {
                row[0] = rs.getObject(1);
                row[1] = rs.getObject(2);
                dtm.addRow(row);
            }
            tabStock.setModel(dtm);
            tabStock.getColumnModel().getColumn(0).setPreferredWidth(300);
            tabStock.getColumnModel().getColumn(1).setPreferredWidth(100);
            rs.close();
            st.close();
        } catch (SQLException e) {
            cg.boiteDeDialogue(e.toString(), "erreur");
        }
    }

