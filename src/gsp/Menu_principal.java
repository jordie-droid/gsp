    public int expire() {
        try {
            vide = "1111-11-11";
            st = new ConnectDataBase().getConnexion().createStatement();
            rs = st.executeQuery("select count(*) from destocker");
            while (rs.next()) {
                count = rs.getInt(1);
            }
            if (count == 0) {
                rs = st.executeQuery("select count(*) from stocker where stocker.dateexp <= '" + madate + "' and stocker.dateexp != '" + vide + "'");
                while (rs.next()) {
                    count = rs.getInt(1);
                }
            } else {
                rs = st.executeQuery("select count(*) from stocker inner join destocker on destocker.codebar != stocker.codebar where stocker.dateexp <= '" + madate + "' and stocker.dateexp != '" + vide + "'");
                while (rs.next()) {
                    count = rs.getInt(1);
                }
            }
            st.close();
            rs.close();
            return count;
        } catch (SQLException e) {
            cg.boiteDeDialogue(e.toString(), "erreur");
            return count;
        }
    }

