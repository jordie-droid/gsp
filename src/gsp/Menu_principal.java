    public void dateheure() {
        Thread clock = new Thread() {
            private int annee;
            private int jour;
            private String month;

            @Override
            public void run() {
                for (;;) {
                    Calendar cal = new GregorianCalendar();
                    int mois = cal.get(Calendar.MONTH);
                    annee = cal.get(Calendar.YEAR);
                    jour = cal.get(Calendar.DAY_OF_MONTH);
                    int seconde = cal.get(Calendar.SECOND);
                    int minute = cal.get(Calendar.MINUTE);
                    int heure = cal.get(Calendar.HOUR_OF_DAY);
                    int jourse = cal.get(Calendar.DAY_OF_WEEK);
                    month = "";
                    String day = "";
                    switch (jourse - 1) {
                        case 1:
                            day = "Lundi";
                            break;
                        case 2:
                            day = "Mardi";
                            break;
                        case 3:
                            day = "Mercredi";
                            break;
                        case 4:
                            day = "Jeudi";
                            break;
                        case 5:
                            day = "Vendredi";
                            break;
                        case 6:
                            day = "Samedi";
                            break;
                        default:
                            day = "Dimanche";
                            break;
                    }

                    switch (mois + 1) {
                        case 1:
                            month = "Janvier";
                            break;
                        case 2:
                            month = "Février";
                            break;
                        case 3:
                            month = "Mars";
                            break;
                        case 4:
                            month = "Avril";
                            break;
                        case 5:
                            month = "Mai";
                            break;
                        case 6:
                            month = "Juin";
                            break;
                        case 7:
                            month = "Juillet";
                            break;
                        case 8:
                            month = "Août";
                            break;
                        case 9:
                            month = "Septembre";
                            break;
                        case 10:
                            month = "Octobre";
                            break;
                        case 11:
                            month = "Novembre";
                            break;
                        case 12:
                            month = "Décembre";
                            break;
                    }
                    int mmois = mois + 1;
                    String m = "", j = "";
                    int mm = mois + 1;
                    String mmmois;
                    if (mm < 10) {
                        mmmois = "0" + mm;
                    } else {
                        mmmois = String.valueOf(mm);
                    }
                    if (jour < 10) {
                        j = "0" + jour;
                    } else {
                        j = String.valueOf(jour);
                    }
                    String h = "", mi = "", s = "";
                    if (heure < 10) {
                        h = "0" + heure;
                    } else {
                        h = String.valueOf(heure);
                    }
                    if (minute < 10) {
                        mi = "0" + minute;
                    } else {
                        mi = String.valueOf(minute);
                    }
                    if (seconde < 10) {
                        s = "0" + seconde;
                    } else {
                        s = String.valueOf(seconde);
                    }
                    labExp.setText(String.valueOf(expire()));
                    labDes.setText(String.valueOf(destocker()));
                    labSeuil.setText(String.valueOf(seuilDalerte()));
                    labDate.setText("" + day + " le" + " " + j + " " + month + " " + annee);
                    labHeure.setText("" + h + ":" + mi + ":" + s);
                    madate = annee + "-" + mmmois + "-" + j;
                    //cg.initialisationFiche(madate);
                    try {
                        sleep(1000);
                    } catch (InterruptedException e) {
                        cg.boiteDeDialogue(e.toString(), "erreur");
                    }
                }
            }
        };
        clock.start();
    }

